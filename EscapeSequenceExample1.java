public class EscapeSequenceExample {
    public static void main(String[] args) {
        System.out.println("Hello, World!\n");       // New line
        System.out.println("Hello,\tWorld!");        // Tab
        System.out.println("This is a backslash: \\");// Backslash
        System.out.println("She said, \"Hello!\"");  // Double quote
        System.out.println("It\'s a beautiful day!"); // Single quote
    }
}